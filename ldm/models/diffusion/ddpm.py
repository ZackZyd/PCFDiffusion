    #   对输入数据进行第一阶段的编码
    @torch.no_grad()
    def encode_first_stage(self, x):
        #该方法根据是否启用分块分布式向量量化（patch-distributed VQ）来选择不同的编码方式。
        #如果启用了分块分布式向量量化，将对输入数据进行分块处理，并在编码过程中对每个块进行编码。如果未启用，将直接对整个输入数据进行编码。返回的结果是编码后的数据。
        if hasattr(self, "split_input_params"):     #   检查是否存在名为split_input_params的属性。
            if self.split_input_params["patch_distributed_vq"]: #   如果存在且split_input_params["patch_distributed_vq"]为True，则执行以下操作
                ks = self.split_input_params["ks"]  # eg. (128, 128)    获取split_input_params中的参数值，如ks（kernel size，卷积核大小）、stride（步长）、df（downsampling factor，下采样因子）
                stride = self.split_input_params["stride"]  # eg. (64, 64)
                df = self.split_input_params["vqf"]
                self.split_input_params['original_image_size'] = x.shape[-2:]
                bs, nc, h, w = x.shape
                if ks[0] > h or ks[1] > w:  #   检查输入数据x的尺寸，并根据尺寸与ks、stride的比较进行调整，确保ks和stride不超过输入数据的尺寸
                    ks = (min(ks[0], h), min(ks[1], w))
                    print("reducing Kernel")

                if stride[0] > h or stride[1] > w:
                    stride = (min(stride[0], h), min(stride[1], w))
                    print("reducing stride")

                fold, unfold, normalization, weighting = self.get_fold_unfold(x, ks, stride, df=df) #   调用get_fold_unfold方法，根据输入数据的尺寸、ks、stride等参数获取fold、unfold、normalization和weighting
                z = unfold(x)  # (bn, nc * prod(**ks), L)   将输入数据x展开成形状为(bn, nc * prod(**ks), L)的张量z
                # Reshape to img shape
                z = z.view((z.shape[0], -1, ks[0], ks[1], z.shape[-1]))  # (bn, nc, ks[0], ks[1], L )   将z进行形状变换，变为(bn, nc, ks[0], ks[1], L)的张量。

                output_list = [self.first_stage_model.encode(z[:, :, :, :, i])
                               for i in range(z.shape[-1])] #   遍历z的最后一个维度，对每个位置的子张量调用self.first_stage_model.encode方法进行编码，得到编码结果列表output_list。

                o = torch.stack(output_list, axis=-1)   #   将output_list中的编码结果张量按最后一个维度进行堆叠，得到形状为(bn, nc * ks[0] * ks[1], L)的张量o
                o = o * weighting   #   将o乘以weighting进行加权

                # Reverse reshape to img shape
                o = o.view((o.shape[0], -1, o.shape[-1]))  # (bn, nc * ks[0] * ks[1], L)    将o进行形状变换，变为(bn, nc * ks[0] * ks[1], L)的张量。
                # stitch crops together
                decoded = fold(o)       #   调用fold方法，对编码结果进行拼接，得到解码结果decoded。
                decoded = decoded / normalization   #   将decoded除以normalization进行归一化
                return decoded

            else:
                return self.first_stage_model.encode(x)
        else:
            return self.first_stage_model.encode(x)

